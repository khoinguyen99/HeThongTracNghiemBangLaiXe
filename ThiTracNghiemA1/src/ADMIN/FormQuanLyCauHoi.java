    private void jThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jThemActionPerformed

        String query;

        if (jMACAUHOI.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa nhập mã câu hỏi");
            return;
        }
        if (jCAUHOI.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa nhập câu hỏi");
            return;
        }

        query = "Insert into CAU_HOI(MACAUHOI,CAUHOI,DAPAN1,DAPAN2,DAPAN3,DAPAN4,DAPANDUNG,HINHANHCAUHOI) values ('" + jMACAUHOI.getText() + "','" + jCAUHOI.getText() + "','" + jDAPAN1.getText()
                + "','" + jDAPAN2.getText() + "','" + jDAPAN3.getText() + "','" + jDAPAN4.getText() + "','" + jDAPANDUNG.getText() + "','" + jHINHANHCAUHOI.getText() + "')";

        con.UpdateData(query);
        showTable();

    }//GEN-LAST:event_jThemActionPerformed

