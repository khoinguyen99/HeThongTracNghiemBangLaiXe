    private void jTableCauHoiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableCauHoiMouseClicked
        int index = jTableCauHoi.getSelectedRow();

        jMACAUHOI.setText(dsCH.get(index).getMaCauHoi());
        jCAUHOI.setText(dsCH.get(index).getCauHoi());
        jDAPAN1.setText(dsCH.get(index).getDapAn1());
        jDAPAN2.setText(dsCH.get(index).getDapAn2());
        jDAPAN3.setText(dsCH.get(index).getDapAn3());
        jDAPAN4.setText(dsCH.get(index).getDapAn4());
        jDAPANDUNG.setText(dsCH.get(index).getDapAnDung());
        jHINHANHCAUHOI.setText(dsCH.get(index).getHinhAnhCauHoi());

    }//GEN-LAST:event_jTableCauHoiMouseClicked

